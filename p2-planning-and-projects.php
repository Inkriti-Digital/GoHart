							<h2>TRANSITION OF HART FLEET TO COMPRESSED NATURAL GAS <br/>
							COMPRESSED NATURAL GAS TRANSITION
							</h2>
							<p>
								HART is transitioning our fleet of buses and vans to Compressed Natural Gas (CNG), with plans to have the full fleet operating on CNG by 2026.
							</p>

							<h2>WHAT IS COMPRESSED NATURAL GAS?</h2>
							<p>CNG is a fossil fuel comprised mostly of methane (around 90%), small amounts of ethane, propane and other gases. It is lighter than air and burns almost completely. CNG combustion results in leftover carbon dioxide and water, the same molecular compound we exhale when we breathe. Natural gas can be used in the form of compressed natural gas (CNG) or liquefied natural gas (LNG).</p>

							<h2>WHY COMPRESSED NATURAL GAS?</h2>
							<p><b>Reduction of CO<sup>2</sup>  Emissions</b><br/>
								CNG vehicles produce the fewest emissions and contain significantly less pollutants than gasoline. Natural gas produces 30% to 40% less CO<sup>2</sup>, which is a greenhouse gas, and carbon monoxide (CO) is reduced by more than 90%.
								<ul>
									<li>
										
									</li>
									<li>
										One HART CNG van is estimated to reduce CO<sup>2</sup> emissions by 9 tons per year.  With 28 CNG vans in operation in 2014, HART will reduce these emissions by 252 tons per year.
									</li>
									<li>
										One HART CNG bus will reduce CO<sup>2</sup>  by 25 tons per year. HART is scheduled to have 22 CNG buses in operation in 2015; thereby reducing emissions by 550 tons.
									</li>
								</ul>
								Imagine how much CO<sup>2</sup>  emissions will be reduced by HART when the full fleet is converted to Compressed Natural Gas.
							</p>
							<p><b>American-made</b><br/>
								HART is doing its part. More than 99% of the natural gas used in the United States comes from domestic or other American sources. The United States imports over one billion dollars of crude oil every day. Estimates show the United States has well over a 100 year supply of natural gas, significantly lessening dependence on foreign oil supplies. The CNG industry helps support national security, U.S. jobs and the US economy. </p>

